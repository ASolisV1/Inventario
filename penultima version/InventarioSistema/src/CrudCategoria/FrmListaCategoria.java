    public void preTabla(){
        QueryCategoria prueba= new QueryCategoria();
        ArrayList <ClsCategoria> categorias = prueba.marcasArr();
        initTabla(categorias);
    }
    
    public void initTabla(ArrayList <ClsCategoria> m){        
        String[] columnNames = {"Nro.","Codigo","Nombre de la categoria",};
        DefaultTableModel modelo = new DefaultTableModel(null,columnNames);       
        jTable1.setModel(modelo);
        
        for(int  i=0; i<m.size();i++){
            String[] fila={Integer.toString(i+1),Integer.toString(m.get(i).getCodCateg()),m.get(i).getNomCateg()};
            modelo.addRow(fila);
        }
    }
    
    
    
