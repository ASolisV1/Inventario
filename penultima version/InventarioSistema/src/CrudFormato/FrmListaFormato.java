    public void initTabla(ArrayList <ClsFormato> m){        
        String[] columnNames = {"Nro.","Codigo","Nombre de formato",};
        DefaultTableModel modelo = new DefaultTableModel(null,columnNames);       
        jTable1.setModel(modelo);
        
        for(int  i=0; i<m.size();i++){
            String[] fila={Integer.toString(i+1),Integer.toString(m.get(i).getCodFor()),m.get(i).getNomFor()};
            modelo.addRow(fila);
        }
    }
    
    
    
